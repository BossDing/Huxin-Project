package com.huyan.pojo.vo;

public class MyFriendsVO {
    private String friendUserId;
    private String friendUsername;
    private String friendFaceImage;
    private String friendNickname;
    /**
	 * @return the friendUserId
	 */
	public String getFriendUserId() {
		return friendUserId;
	}
	/**
	 * @param friendUserId the friendUserId to set
	 */
	public void setFriendUserId(String friendUserId) {
		this.friendUserId = friendUserId;
	}
	/**
	 * @return the friendUsername
	 */
	public String getFriendUsername() {
		return friendUsername;
	}
	/**
	 * @param friendUsername the friendUsername to set
	 */
	public void setFriendUsername(String friendUsername) {
		this.friendUsername = friendUsername;
	}
	/**
	 * @return the friendFaceImage
	 */
	public String getFriendFaceImage() {
		return friendFaceImage;
	}
	/**
	 * @param friendFaceImage the friendFaceImage to set
	 */
	public void setFriendFaceImage(String friendFaceImage) {
		this.friendFaceImage = friendFaceImage;
	}
	public String getFriendNickname() {
		return friendNickname;
	}
	public void setFriendNickname(String friendNickname) {
		this.friendNickname = friendNickname;
	}
}